
# ----------------Binary AND--------------

a = 50       # 110010 (Binary out value of 50)
b = 25       # 011001 (Binary out value of 25)
c = a & b    # 010000 (Binary out value of 16)

print(c)

# -----------Binary RIGHT SHIFT --------------------

x = 240        # 11110000
y = x >> 2     # 00111100

print(y)
